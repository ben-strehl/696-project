# The goal for this level is to produce 10 sugar.
# We could simply type 'add_Sugar()' 10 times
# but that is slow.
# Good programmers are lazy and find ways to save time.
# We will use a loop to save some typing.
# Type the following:
# for i in range(10):
#     add_Sugar()

# Note that the second line 'add_Sugar()' must be indented.
# Indentation is very important in Python, so pay 
# attention to it.
# This is called a 'for' loop, because it loops 'for' a set
# number of times.
# We use the range() function to specify how many times 
# we want to loop.
# 'range(10)' is an example of passing an argument to a
# function, where '10' is the argument.